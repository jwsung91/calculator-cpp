#include "calculator.hpp"

int Calculator::Plus(int var1, int var2)
{
    return var1 + var2;
}

int Calculator::Minus(int var1, int var2)
{
    return var1 - var2;
}

int Calculator::Divide(int var1, int var2)
{
    return var1 / var2;
}

int Calculator::Multiply(int var1, int var2)
{
    return var1 * var2;
}
